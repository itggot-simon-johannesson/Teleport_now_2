
function js_toggle_in_switch() {
    element = document.querySelector(".in_switch")
    element.classList.toggle("in_switch_toggle")
}

function js_toggle_switch() {
    element = document.querySelector(".switch")
    element.classList.toggle("switch_toggle")
}

function js_toggle_favourites() {
    element = document.querySelector(".favourites")
    element.classList.toggle("favourites_toggle")
}
